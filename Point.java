package com.example.robbesteynen.wv;

public class Point {
    private Double xCoord;
    private Double yCoord;
    public Point(Double x, Double y){
        this.xCoord = x;
        this.yCoord = y;
    }

    public Double getxCoord() {
        return xCoord;
    }

    public Double getyCoord() {
        return yCoord;
    }
}
