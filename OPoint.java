package ar.wv;

public class OPoint {
    private Double xCoord;
    private Double yCoord;
    public OPoint(Double x, Double y){
        this.xCoord = x;
        this.yCoord = y;
    }

    public Double getxCoord() {
        return xCoord;
    }

    public Double getyCoord() {
        return yCoord;
    }
}
